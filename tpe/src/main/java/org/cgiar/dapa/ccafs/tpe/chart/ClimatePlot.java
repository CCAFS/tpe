package org.cgiar.dapa.ccafs.tpe.chart;

import java.util.LinkedList;
import java.util.List;

/**
 * The class that provides the climate data for ploting the column plots
 * 
 * @author nmatovu
 *
 */
public class ClimatePlot {

	private List<List<Object>> data = new LinkedList<List<Object>>();

	private String name;

	public ClimatePlot(String name, List<List<Object>> data) {
		super();
		this.name = name;
		this.data = data;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List<List<Object>> getData() {
		return data;
	}

	public void setData(List<List<Object>> data) {
		this.data = data;
	}

}
